
package ex1.dto;

public class GogekDTO {
    private int gbun;
    private String goname;
    private String gotel;
    private String gojumin;
    private int godam;

    public int getGbun() {
        return gbun;
    }

    public void setGbun(int gbun) {
        this.gbun = gbun;
    }

    public String getGoname() {
        return goname;
    }

    public void setGoname(String goname) {
        this.goname = goname;
    }

    public String getGotel() {
        return gotel;
    }

    public void setGotel(String gotel) {
        this.gotel = gotel;
    }

    public String getGojumin() {
        return gojumin;
    }

    public void setGojumin(String gojumin) {
        this.gojumin = gojumin;
    }

    public int getGodam() {
        return godam;
    }

    public void setGodam(int godam) {
        this.godam = godam;
    }
    
}
